
public class Uniary {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		int c=-15;
		int d=21;
		System.out.println(a++);
		System.out.println(a);
		System.out.println(++a);
		System.out.println("*******************");
		System.out.println(b);
		System.out.println(b--);
		System.out.println(b);
		System.out.println("***********************");
		System.out.println(--b);
		System.out.println(b);
		System.out.println("************");
		System.out.println(~d);
		System.out.println(~c);
		System.out.println("**************");
		boolean e=true;
		boolean f=false;
		System.out.println(!e);//(opposite  of boolan value)
		System.out.println(!f);
		
		
	}
}
