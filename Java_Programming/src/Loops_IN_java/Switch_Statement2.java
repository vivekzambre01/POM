public class Switch_Statement2 {

	public static void main(String[] args) {
		
		char  ch='o';
		switch(ch)
		{
		case 'a':
        System.out.println("vovel");
        break;
		case 'b':
			System.out.println("vovel");
			break;
		case 'c':
			System.out.println("vovel");
			break;
		case 'd':
			System.out.println("vovel");
			break;
		case 'o':
			System.out.println("vovel");
			break;
		default :
			System.out.println("all conconant");
		
		}
	}

}
